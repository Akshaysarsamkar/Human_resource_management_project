package entity;

public class Massage {
	
	private String msg;
	private String type;
	
	
	public Massage(String msg, String type) {
		super();
		this.msg = msg;
		this.type = type;
	}


	public String getMsg() {
		return msg;
	}


	public void setMsg(String msg) {
		this.msg = msg;
	}


	public String getType() {
		return type;
	}


	public void setType(String type) {
		this.type = type;
	}
	
	
	
	

}
